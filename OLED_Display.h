void display_on_OLED(const GFXfont *font_style, uint8_t font_size, int16_t x, int16_t y, String text) {
  display.setTextSize(font_size);
  display.setTextColor(SH110X_WHITE, SH110X_BLACK);
  display.setFont(font_style);
  display.setCursor(x, y);
  display.print(text);
  display.display();
}

void display_temp_on_OLED(const GFXfont *font_style, uint8_t font_size_1, uint8_t font_size_2, int16_t x[3], int16_t y[3], String text_1, String text_2, String text_3) {
  display.setTextColor(SH110X_WHITE, SH110X_BLACK);
  display.setTextColor(SH110X_INVERSE);
  display.setTextSize(font_size_1);
  display.setFont(font_style);
  display.setCursor(x[0], y[0]);
  display.print(text_1);
  display.setTextSize(font_size_2);
  display.setFont(font_style);
  display.setCursor(x[1], y[1]);
  display.print(text_2);
  display.setTextSize(font_size_1);
  display.setFont(font_style);
  display.setCursor(x[2], y[2]);
  display.print(text_3);
  display.display();

}
